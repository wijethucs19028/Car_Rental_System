    private void txtcust_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcust_idKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String cust_id = txtcust_id.getText();
            try {
                pst2 = connection.prepareStatement("select * from customer where nic= ?");
                pst2.setString(1, cust_id);
                re1 = pst2.executeQuery();
                if (re1.next() == false) {
                    JOptionPane.showMessageDialog(this, "customer not found");
                } else {
                    String custname = re1.getString("customerName");
                    txtcust_name.setText(custname.trim());
                }

            } catch (SQLException ex) {
                Logger.getLogger(RentalRegistation.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_txtcust_idKeyPressed

    private void OkbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OkbtnActionPerformed

        getFormData();
        boolean isEmpty = isFormEmpty(carRegNo, customerId, customerName, rentalFee, date, dueDate);
        if (isEmpty) {
            JOptionPane.showMessageDialog(this, "FILL THE ALL FEILDS");
        } else {
            Rental rental = new Rental(carRegNo, customerId, customerName, rentalFee, date, dueDate);
            int count = rentalcrud.insertRental(rental);
            if (count != 0) {
                JOptionPane.showMessageDialog(this, "successfully inserted");
                clearForm();
               txtavl.setText("No");
            }
        }
    }//GEN-LAST:event_OkbtnActionPerformed

