    private void getFormData() {
        carRegNo = combocarno.getSelectedItem().toString();
        customerId = txtcust_id.getText();
        customerName = txtcust_name.getText();
        rentalFee = txtrentalfee.getText();
        SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-mm-dd");
        date = date_format1.format(txtdate.getDate());
        SimpleDateFormat date_format2 = new SimpleDateFormat("yyyy-mm-dd");
        dueDate = date_format2.format(txtdue.getDate());
    }

    private boolean isFormEmpty(String carRegno, String customerId, String customerName, String rentalFee, String data, String dueDate) {
        boolean isEmpty = false;
        if (carRegno.equals("") || customerId.equals("") || customerName.equals("") || rentalFee.equals("") || data.equals("") || dueDate.equals("")) {
            isEmpty = true;
        }
        return isEmpty;
    }

