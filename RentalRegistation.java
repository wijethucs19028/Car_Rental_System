    private void combocarnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combocarnoActionPerformed

        String carRegNo = combocarno.getSelectedItem().toString();
        try {

            String query = "select * from car where RegistationNo = ?";
            pst1 = connection.prepareStatement(query);
            pst1.setString(1, carRegNo);
            re2 = pst1.executeQuery();

            if (re2.next() == false) {
                JOptionPane.showMessageDialog(this, "car no found");
            } else {
                String aval = re2.getString(5);
                txtavl.setText(aval.trim());

                if (aval.equals("Yes")) {
                    txtcust_id.setEnabled(true);
                    txtcust_name.setEnabled(true);
                    txtrentalfee.setEnabled(true);
                    txtdate.setEnabled(true);
                    txtdue.setEnabled(true);
                } else {
                    txtcust_id.setEnabled(false);
                    txtcust_name.setEnabled(false);
                    txtrentalfee.setEnabled(false);
                    txtdate.setEnabled(false);
                    txtdue.setEnabled(false);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(RentalRegistation.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_combocarnoActionPerformed

