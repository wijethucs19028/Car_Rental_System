    public void autoId() {
        connection = DatabaseConnection.createDatabseConnection();
        String query = "select Max(carId) from car";
        try {
            Statement s = connection.createStatement();
            ResultSet rs = s.executeQuery(query);
            rs.next();
            rs.getString("Max(carId)");
            if (rs.getString("Max(carId)") == null) {
                carIdText.setText("001");

            } else {
                long id = Long.parseLong(rs.getString("Max(carId)"));
                id++;
                carIdText.setText(String.format("%01d", id));
            }

        } catch (SQLException ex) {
            Logger.getLogger(CarRegistation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void updateTable() {
        try {
            ResultSet result = carCrud.retrievedAll();
            ResultSetMetaData resultSetMetaData = result.getMetaData();
            defaultTableModel = (DefaultTableModel) carTable.getModel();
            defaultTableModel.setRowCount(0);

            while (result.next()) {
                Vector vector = new Vector();
                for (int i = 1; i <= resultSetMetaData.getColumnCount(); i++) {
                    vector.add(result.getString(1));
                    vector.add(result.getString(2));
                    vector.add(result.getString(3));
                    vector.add(result.getString(4));
                    vector.add(result.getString(5));
                }
                defaultTableModel.addRow(vector);
            }

        } catch (SQLException ex) {
            Logger.getLogger(CarRegistation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean isFormEmpty(String carID, String registationNo, String brand, String model) {
        boolean isEmpty = false;
        if (carID.equals("") || registationNo.equals("")
                || brand.equals("")
                || model.equals("")) {
            isEmpty = true;
        }

        return isEmpty;
    }

