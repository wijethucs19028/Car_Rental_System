    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        getFormData();
        boolean isEmpty = isFormEmpty(carIdText.getText(), registationNo, brand, model);
        if (isEmpty) {
            JOptionPane.showMessageDialog(this, "FILL THE ALL FEILDS");
        } else {
            //creating car object
            Car car = new Car(carID, registationNo, brand, model, available);
            //pass the car object to carCrud
            int count = carCrud.insert(car);
            if (count != 0) {
                JOptionPane.showMessageDialog(this, "Record inserted");
                clearForm();
                autoId();
            }

            updateTable();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // delete car
        defaultTableModel = (DefaultTableModel) carTable.getModel();
        selectedIndex = carTable.getSelectedRow();

        int isDeleting = JOptionPane.showConfirmDialog(this,
                "Do you Want to delete the record",
                "Warning",
                JOptionPane.YES_NO_OPTION);

        if (isDeleting == JOptionPane.YES_OPTION) {
            getFormData();
            int count = carCrud.deleteCar(carID);
            if (count != 0) {
                JOptionPane.showMessageDialog(this, "Record Deleted");

                clearForm();
                autoId();
            }
            updateTable();
        } else {
            JOptionPane.showMessageDialog(this, "Record Not Deleted");
        }

    }//GEN-LAST:event_jButton6ActionPerformed

